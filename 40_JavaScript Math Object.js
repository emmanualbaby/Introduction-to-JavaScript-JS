// JavaScript Math Object

// Math.PI
let pi = Math.PI;
console.log("Value of PI: " + pi); // Output: Value of PI: 3.141592653589793

// Math.round()
let rounded = Math.round(4.7);
console.log("Rounded value: " + rounded); // Output: Rounded value: 5

// Math.ceil()
let ceilValue = Math.ceil(4.1);
console.log("Ceil value: " + ceilValue); // Output: Ceil value: 5

// Math.floor()
let floorValue = Math.floor(4.9);
console.log("Floor value: " + floorValue); // Output: Floor value: 4

// Math.trunc()
let truncValue = Math.trunc(4.9);
console.log("Trunc value: " + truncValue); // Output: Trunc value: 4

// Math.sign()
let signValue = Math.sign(-5);
console.log("Sign value: " + signValue); // Output: Sign value: -1

// Math.pow()
let power = Math.pow(2, 3);
console.log("2 to the power of 3: " + power); // Output: 2 to the power of 3: 8

// Math.sqrt()
let squareRoot = Math.sqrt(16);
console.log("Square root of 16: " + squareRoot); // Output: Square root of 16: 4

// Math.abs()
let absolute = Math.abs(-7.25);
console.log("Absolute value: " + absolute); // Output: Absolute value: 7.25

// Math.sin()
let sine = Math.sin(Math.PI / 2);
console.log("Sine of PI/2: " + sine); // Output: Sine of PI/2: 1

// Math.cos()
let cosine = Math.cos(0);
console.log("Cosine of 0: " + cosine); // Output: Cosine of 0: 1

// Math.min()
let minValue = Math.min(0, 150, 30, 20, -8, -200);
console.log("Minimum value: " + minValue); // Output: Minimum value: -200

// Math.max()
let maxValue = Math.max(0, 150, 30, 20, -8, -200);
console.log("Maximum value: " + maxValue); // Output: Maximum value: 150

// Math.random()
let randomValue = Math.random();
console.log("Random value: " + randomValue); // Output: Random value: (a random number between 0 and 1)

// Math.log()
let logValue = Math.log(1);
console.log("Natural logarithm of 1: " + logValue); // Output: Natural logarithm of 1: 0

// Math.exp()
let expValue = Math.exp(1);
console.log("Exponential of 1: " + expValue); // Output: Exponential of 1: 2.718281828459045
