// JS Assignment - Example
let x = 10; // Assign value using =
x += 5;     // Add and assign using +=
x -= 3;     // Subtract and assign using -=
x *= 2;     // Multiply and assign using *=
x /= 2;     // Divide and assign using /=
x %= 3;     // Modulus and assign using %=

console.log("Result: " + x); // Print the result
