// JS Arithmetic - Example
let a = 10;
let b = 20;
let sum = a + b;
let diff = a - b;
let prod = a * b;
let quot = a / b;
let mod = a % b;

console.log("Sum: " + sum);
console.log("Difference: " + diff);
console.log("Product: " + prod);
console.log("Quotient: " + quot);
console.log("Modulus: " + mod);
